#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;

	if (!s && !n)
		return ;
	p = (unsigned char *)s;
	while (n > 0)
	{
		*(p++) = 0;
		n--;
	}
}
